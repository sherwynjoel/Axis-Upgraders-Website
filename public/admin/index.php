$message = "";
$errors = [];

if ($_SERVER['REQUEST_METHOD'] === 'POST' && isset($_POST['save'])) {
    // ── Image uploads ──
    foreach ($image_slots as $field => $dest) {
        if (!isset($_FILES[$field]) || $_FILES[$field]['error'] === UPLOAD_ERR_NO_FILE) continue;
        $f = $_FILES[$field];
        if ($f['error'] !== UPLOAD_ERR_OK) {
            $errors[] = "Upload failed for $field (PHP error code {$f['error']}).";
            continue;
        }
        if (@getimagesize($f['tmp_name']) === false) {
            $errors[] = "$field: uploaded file is not a valid image.";
            continue;
        }
        $dest_dir = dirname($dest);
        if (!is_dir($dest_dir)) @mkdir($dest_dir, 0775, true);
        if (!move_uploaded_file($f['tmp_name'], $dest)) {
            $errors[] = "$field: could not write to $dest. Check folder permissions.";
        }
    }

    // ── Text/JSON updates ──
    if (isset($_POST['data'])) {
        // Variable-length arrays: validate and replace wholesale
        // (update_array_from_post only walks existing keys, so adds wouldn't take.)

        // services.items
        if (isset($_POST['data']['services']['items'])) {
            $clean = [];
            foreach (array_values($_POST['data']['services']['items']) as $item) {
                if (!is_array($item)) continue;
                $title = trim($item['title'] ?? '');
                $description = trim($item['description'] ?? '');
                $icon = $item['icon'] ?? 'Hammer';
                if ($title === '' && $description === '') continue;
                if (!array_key_exists($icon, $service_icons)) $icon = 'Hammer';
                $clean[] = ['title' => $title, 'description' => $description, 'icon' => $icon];
            }
            $content['services']['items'] = $clean;
            unset($_POST['data']['services']['items']);
        }

        // trust_strip
        if (isset($_POST['data']['trust_strip'])) {
            $content['trust_strip'] = clean_string_array($_POST['data']['trust_strip']);
            unset($_POST['data']['trust_strip']);
        }

        // why.items
        if (isset($_POST['data']['why']['items'])) {
            $content['why']['items'] = clean_string_array($_POST['data']['why']['items']);
            unset($_POST['data']['why']['items']);
        }

        // results.items
        if (isset($_POST['data']['results']['items'])) {
            $content['results']['items'] = clean_string_array($_POST['data']['results']['items']);
            unset($_POST['data']['results']['items']);
        }

        // process.steps
        if (isset($_POST['data']['process']['steps'])) {
            $content['process']['steps'] = clean_step_array($_POST['data']['process']['steps']);
            unset($_POST['data']['process']['steps']);
        }

        update_array_from_post($content, $_POST['data']);

        if (!file_put_contents($content_file, json_encode($content, JSON_PRETTY_PRINT))) {
            $errors[] = "Failed to write site-content.json. Check file permissions.";
        }
    }

    $message = empty($errors) ? "Changes saved successfully!" : implode(' ', $errors);
}

