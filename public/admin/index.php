function clean_string_array($raw) {
    if (!is_array($raw)) return [];
    $clean = [];
    foreach (array_values($raw) as $val) {
        if (!is_string($val)) continue;
        $val = trim($val);
        if ($val === '') continue;
        $clean[] = $val;
    }
    return $clean;
}

function clean_step_array($raw) {
    if (!is_array($raw)) return [];
    $clean = [];
    foreach (array_values($raw) as $item) {
        if (!is_array($item)) continue;
        $title = trim($item['title'] ?? '');
        $description = trim($item['description'] ?? '');
        if ($title === '' && $description === '') continue;
        $clean[] = ['title' => $title, 'description' => $description];
    }
    return $clean;
}

function update_array_from_post(&$target, $post_data) {
    foreach ($post_data as $key => $value) {
        if (is_array($value) && isset($target[$key])) {
            update_array_from_post($target[$key], $value);
        } else if (isset($target[$key])) {
            $target[$key] = $value;
        }
    }
}

