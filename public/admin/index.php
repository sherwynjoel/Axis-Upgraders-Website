    <script>
        // Escape special regex characters in a string so it can be used inside a RegExp literal.
        function escapeRegex(s) {
            return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
        }

        function reindexList(listId) {
            const list = document.getElementById(listId);
            if (!list) return;
            const prefix = list.dataset.namePrefix; // e.g. "data[trust_strip]"
            const escaped = escapeRegex(prefix);
            const pattern = new RegExp(escaped + '\\[\\d+\\]');
            list.querySelectorAll('.list-item').forEach((item, i) => {
                item.dataset.index = i;
                const numEl = item.querySelector('.item-num');
                if (numEl) numEl.textContent = i + 1;
                item.querySelectorAll('[name]').forEach(el => {
                    el.name = el.name.replace(pattern, prefix + '[' + i + ']');
                });
            });
        }

        function addListItem(listId, templateId) {
            const list = document.getElementById(listId);
            const tpl  = document.getElementById(templateId);
            if (!list || !tpl) return;
            const newIndex = list.querySelectorAll('.list-item').length;
            const clone = tpl.content.cloneNode(true);
            clone.querySelectorAll('[name]').forEach(el => {
                el.name = el.name.replace(/__INDEX__/g, newIndex);
            });
            const wrapper = clone.querySelector('.list-item');
            wrapper.dataset.index = newIndex;
            const numEl = wrapper.querySelector('.item-num');
            if (numEl) numEl.textContent = newIndex + 1;
            list.appendChild(clone);
        }

        function removeListItem(btn, listId) {
            const item = btn.closest('.list-item');
            if (!item) return;
            const list = document.getElementById(listId);
            const remaining = list.querySelectorAll('.list-item').length;
            if (remaining <= 1) {
                alert('At least one item is required.');
                return;
            }
            if (!confirm('Remove this item?')) return;
            item.remove();
            reindexList(listId);
        }
